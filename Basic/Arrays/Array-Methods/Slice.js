//slice(start,end)
//returns a new array containing all items from index 'start' to index 'end'.both can be -ve.
let str = "test";
let arr = ["t", "e", "s", "t"];
console.log(str.slice(1, 3)); //es
console.log(arr.slice(1, 3)); //['e','s']
console.log(str.slice(-2)); //st
console.log(arr.slice(-2)); //['s','t']
